/**
 * FeedForge — RSS XML 생성기
 *
 * feed 라이브러리를 사용하여 표준 RSS 2.0 XML을 생성합니다.
 */

const { Feed } = require('feed');
const fs = require('fs');
const path = require('path');

const OUTPUT_DIR = path.resolve(__dirname, '..', 'docs', 'feeds');

/**
 * 필터링된 게시물 목록으로 RSS XML 파일을 생성합니다.
 *
 * @param {Array} items - 게시물 배열
 * @param {object} outputConfig - 출력 설정 { filename, title, description, link, maxItems }
 * @param {boolean} dryRun - true면 파일 쓰기 없이 XML 반환만
 * @returns {string} 생성된 RSS XML 문자열
 */
function generateRSS(items, outputConfig, dryRun = false) {
  const {
    filename,
    title = 'FeedForge Feed',
    description = 'Generated by FeedForge',
    link = '',
    maxItems = 30,
  } = outputConfig;

  // 최대 개수 제한
  const limitedItems = items.slice(0, maxItems);

  const feed = new Feed({
    title,
    description,
    id: link || `feedforge:${filename}`,
    link: link || '',
    language: 'ko',
    generator: 'FeedForge',
    updated: new Date(),
    copyright: '',
  });

  for (const item of limitedItems) {
    feed.addItem({
      title: item.title,
      id: item.link,
      link: item.link,
      date: item.date ? new Date(item.date) : new Date(),
      author: item.author ? [{ name: item.author }] : [],
      description: buildDescription(item),
    });
  }

  const xml = feed.rss2();

  if (!dryRun && filename) {
    // 출력 디렉토리 생성
    if (!fs.existsSync(OUTPUT_DIR)) {
      fs.mkdirSync(OUTPUT_DIR, { recursive: true });
    }
    const filePath = path.join(OUTPUT_DIR, filename);
    fs.writeFileSync(filePath, xml, 'utf-8');
    console.log(`  ✓ RSS 저장: docs/feeds/${filename} (${limitedItems.length}개 항목)`);
  }

  return xml;
}

/**
 * 게시물 메타정보를 description에 포함합니다.
 */
function buildDescription(item) {
  const parts = [];
  if (item.no) parts.push(`번호: ${item.no}`);
  if (item.author) parts.push(`작성자: ${item.author}`);
  if (item.views) parts.push(`조회: ${item.views}`);
  if (item.likes) parts.push(`추천: ${item.likes}`);
  return parts.length > 0 ? parts.join(' | ') : item.title;
}

/**
 * 메타데이터 JSON을 생성합니다 (대시보드용).
 */
function generateMeta(feedResults) {
  const meta = {
    generator: 'FeedForge',
    generatedAt: new Date().toISOString(),
    feeds: feedResults.map((r) => ({
      name: r.name,
      filename: r.filename,
      itemCount: r.itemCount,
      status: r.status,
      error: r.error || null,
    })),
  };

  const metaDir = path.resolve(__dirname, '..', 'docs');
  if (!fs.existsSync(metaDir)) {
    fs.mkdirSync(metaDir, { recursive: true });
  }
  fs.writeFileSync(
    path.join(metaDir, 'meta.json'),
    JSON.stringify(meta, null, 2),
    'utf-8'
  );
  console.log(`  ✓ 메타데이터 저장: docs/meta.json`);
  return meta;
}

module.exports = { generateRSS, generateMeta, OUTPUT_DIR };
